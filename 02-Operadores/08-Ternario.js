
// Operador Ternario cumple con la funcion de un if else pero de manera simple

let edad = 20;
let acceso = edad > 17 ? 'Acceso Permitido' : 'Acceso Denegado';
console.log(acceso);

// En este caso la variable edad es mayor a 17 por lo que el resultado sera 'Acceso Permitido'

