// AND, OR , NOT

// Operador AND (&&) - Devuelve true si ambos operandos son true 

let mayor = true;
let suscrito = true;

console.log('operador yy', mayor && suscrito); // false

// Operador OR (||) - Devuelve true si al menos un operando es true

let mayor2 = false;
let suscrito2 = true;

console.log('operador or', mayor2 || suscrito2); // true

// Operador NOT (!) - Devuelve true si el operando es false

let mayor3 = false;
let suscrito3 = true;   

console.log('operador not', !mayor3); // true   



