// AND, OR , NOT

// Operador AND (&&) - Devuelve true si ambos operandos son true 

let mayor = false;
let suscrito = true;

console.log( 'operador and', mayor && suscrito); // false   

// Operador OR (||) - Devuelve true si al menos uno de los operandos es true

console.log( 'operador or', mayor || suscrito); // true

// Operador NOT (!) - Devuelve true si el operando es false

console.log('operador not', !suscrito); // true

